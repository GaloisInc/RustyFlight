static mut mspFrame: [uint16_t; 18] = [0; 18];
static mut rxMspFrameDone: bool = 0 as libc::c_int != 0;
unsafe extern "C" fn rxMspReadRawRC(mut rxRuntimeConfig:
                                        *const rxRuntimeConfig_t,
                                    mut chan: uint8_t) -> uint16_t {
    return mspFrame[chan as usize];
}
/*
 * Called from MSP command handler - mspFcProcessCommand
 */
#[no_mangle]
pub unsafe extern "C" fn rxMspFrameReceive(mut frame: *mut uint16_t,
                                           mut channelCount: libc::c_int) {
    let mut i: libc::c_int = 0 as libc::c_int;
    while i < channelCount {
        mspFrame[i as usize] = *frame.offset(i as isize);
        i += 1
    }
    // Any channels not provided will be reset to zero
    let mut i_0: libc::c_int = channelCount;
    while i_0 < 18 as libc::c_int {
        mspFrame[i_0 as usize] = 0 as libc::c_int as uint16_t;
        i_0 += 1
    }
    rxMspFrameDone = 1 as libc::c_int != 0;
}
unsafe extern "C" fn rxMspFrameStatus(mut rxRuntimeConfig:
                                          *mut rxRuntimeConfig_t) -> uint8_t {
    if !rxMspFrameDone { return RX_FRAME_PENDING as libc::c_int as uint8_t }
    rxMspFrameDone = 0 as libc::c_int != 0;
    return RX_FRAME_COMPLETE as libc::c_int as uint8_t;
}
#[no_mangle]
pub unsafe extern "C" fn rxMspInit(mut rxConfig: *const rxConfig_t,
                                   mut rxRuntimeConfig:
                                       *mut rxRuntimeConfig_t) {
    (*rxRuntimeConfig).channelCount = 18 as libc::c_int as uint8_t;
    (*rxRuntimeConfig).rxRefreshRate = 20000 as libc::c_int as uint16_t;
    (*rxRuntimeConfig).rcReadRawFn =
        Some(rxMspReadRawRC as
                 unsafe extern "C" fn(_: *const rxRuntimeConfig_t, _: uint8_t)
                     -> uint16_t);
    (*rxRuntimeConfig).rcFrameStatusFn =
        Some(rxMspFrameStatus as
                 unsafe extern "C" fn(_: *mut rxRuntimeConfig_t) -> uint8_t);
}
