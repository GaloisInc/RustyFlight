#[no_mangle]
pub unsafe extern "C" fn targetSerialPortFunctionConfig(mut config:
                                                            *mut targetSerialPortFunction_t,
                                                        mut count: size_t) {
    let mut i: size_t = 0 as libc::c_int as size_t;
    while i < count {
        let mut index: libc::c_int =
            findSerialPortIndexByIdentifier((*config.offset(i as
                                                                isize)).identifier);
        if index >= 0 as libc::c_int {
            (*serialConfigMutable()).portConfigs[index as usize].functionMask
                = (*config.offset(i as isize)).function as uint16_t
        }
        i = i.wrapping_add(1)
    };
}
