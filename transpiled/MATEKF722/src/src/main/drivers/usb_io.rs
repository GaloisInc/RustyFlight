static mut usbDetectPin: IO_t = 0 as *const libc::c_void as IO_t;
#[no_mangle]
pub unsafe extern "C" fn usbCableDetectDeinit() {
    IOInit(usbDetectPin, OWNER_FREE, 0 as libc::c_int as uint8_t);
    IOConfigGPIO(usbDetectPin,
                 (0 as libc::c_uint | (0 as libc::c_uint) << 2 as libc::c_int
                      | (0 as libc::c_uint) << 5 as libc::c_int) as
                     ioConfig_t);
    usbDetectPin = 0 as IO_t;
}
#[no_mangle]
pub unsafe extern "C" fn usbCableDetectInit() {
    usbDetectPin =
        IOGetByTag(((1 as libc::c_int + 1 as libc::c_int) << 4 as libc::c_int
                        | 12 as libc::c_int) as ioTag_t);
    IOInit(usbDetectPin, OWNER_USB_DETECT, 0 as libc::c_int as uint8_t);
    IOConfigGPIO(usbDetectPin,
                 (0x1 as libc::c_uint |
                      (0 as libc::c_uint) << 2 as libc::c_int |
                      (0 as libc::c_uint) << 5 as libc::c_int) as ioConfig_t);
}
#[no_mangle]
pub unsafe extern "C" fn usbCableIsInserted() -> bool {
    let mut result: bool = 0 as libc::c_int != 0;
    if !usbDetectPin.is_null() {
        result = IORead(usbDetectPin) as libc::c_int != 0 as libc::c_int
    }
    return result;
}
