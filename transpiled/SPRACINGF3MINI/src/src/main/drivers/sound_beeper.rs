#[no_mangle]
pub unsafe extern "C" fn systemBeep(mut onoff: bool) {
    if beeperFrequency as libc::c_int == 0 as libc::c_int {
        IOWrite(beeperIO,
                if beeperInverted as libc::c_int != 0 {
                    onoff as libc::c_int
                } else { !onoff as libc::c_int } != 0);
    } else { pwmWriteBeeper(onoff); };
}
#[no_mangle]
pub unsafe extern "C" fn systemBeepToggle() {
    if beeperFrequency as libc::c_int == 0 as libc::c_int {
        IOToggle(beeperIO);
    } else { pwmToggleBeeper(); };
}
#[no_mangle]
pub unsafe extern "C" fn beeperInit(mut config: *const beeperDevConfig_t) {
    beeperFrequency = (*config).frequency;
    if beeperFrequency as libc::c_int == 0 as libc::c_int {
        beeperIO = IOGetByTag((*config).ioTag);
        beeperInverted = (*config).isInverted != 0;
        if !beeperIO.is_null() {
            IOInit(beeperIO, OWNER_BEEPER, 0 as libc::c_int as uint8_t);
            IOConfigGPIO(beeperIO,
                         if (*config).isOpenDrain as libc::c_int != 0 {
                             (GPIO_Mode_OUT as libc::c_int |
                                  (0 as libc::c_int) << 2 as libc::c_int |
                                  (GPIO_OType_OD as libc::c_int) <<
                                      4 as libc::c_int) |
                                 (GPIO_PuPd_NOPULL as libc::c_int) <<
                                     5 as libc::c_int
                         } else {
                             (GPIO_Mode_OUT as libc::c_int |
                                  (0 as libc::c_int) << 2 as libc::c_int |
                                  (GPIO_OType_PP as libc::c_int) <<
                                      4 as libc::c_int) |
                                 (GPIO_PuPd_NOPULL as libc::c_int) <<
                                     5 as libc::c_int
                         } as ioConfig_t);
        }
        systemBeep(0 as libc::c_int != 0);
    } else {
        let beeperTag: ioTag_t = (*config).ioTag;
        beeperPwmInit(beeperTag, beeperFrequency);
    };
}
